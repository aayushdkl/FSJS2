//Demonstrate the use of trim() to remove any trailing whitespace at the beginning and the end of a string.

let first_name = '  aayush  ';
console.log(first_name);
console.log(first_name.trim());


//for just the beginning removal of space
let first_name2 = '  aayush  ';
console.log(first_name2.trimStart());


//for the end ones
let first_name3 = '  aayush  ';
console.log(first_name2.trimEnd());


